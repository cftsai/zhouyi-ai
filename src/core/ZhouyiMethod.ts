export class ZhouyiMethod extends BaseFortuneMethod {
  constructor() {
    super('Zhouyi', 'Traditional Chinese Zhouyi divination method');
  }

  async execute(question: string): Promise<FortuneResult> {
    const hexagram = this.coinCastMethod();
    return {
      question,
      answer: this.generateAnswer(hexagram),
      method: this.name,
      timestamp: new Date(),
      details: {
        originalHexagram: hexagram,
        transformedHexagram: hexagram.transformedHexagram
      }
    };
  }

  coinCastMethod(): ZhouyiHexagram {
    const lines: HexagramLine[] = [];
    for (let i = 1; i <= 6; i++) {
      const coins = [
        Math.random() < 0.5 ? 2 : 3,
        Math.random() < 0.5 ? 2 : 3,
        Math.random() < 0.5 ? 2 : 3
      ];
      const sum = coins.reduce((a, b) => a + b, 0);
      const isYang = sum % 2 === 1;
      const isChanging = sum === 6 || sum === 9;
      lines.push({
        position: i,
        isChanging,
        text: isYang ? (isChanging ? '九（动）' : '九') : (isChanging ? '六（动）' : '六')
      });
    }
    const binaryStr = lines.map(l => (l.text.includes('九') ? '1' : '0')).join('');
    const name = hexagramMap.get(binaryStr) || '未知卦';
    const meaning = hexagramMeanings[name] || '';
    const trigrams = ['乾', '坤', '震', '巽', '坎', '离', '艮', '兑'];
    const upperLines = lines.slice(0, 3);
    const lowerLines = lines.slice(3);
    const upperTrigram = trigrams[this.getTrigramIndex(upperLines)] || '乾';
    const lowerTrigram = trigrams[this.getTrigramIndex(lowerLines)] || '乾';
    const changingLines = lines.filter(l => l.isChanging).map(l => l.position);
    const yaoTextArray = yaoTexts[name] || lines.map((l, i) => `第${i + 1}爻：${l.text}`);
    const detailedLines = lines.map((l, i) => ({
      ...l,
      text: yaoTextArray[i] || l.text
    }));
    let hexagramNumber = 0;
    for (const [key, val] of hexagramData) {
      hexagramNumber++;
      if (val === name) break;
    }

    let transformedHexagram: ZhouyiHexagram | undefined;
    if (changingLines.length > 0) {
      transformedHexagram = this.createTransformedHexagram(lines);
    }

    return {
      upperTrigram,
      lowerTrigram,
      hexagramNumber,
      name,
      meaning,
      lines: detailedLines,
      changingLines,
      transformedHexagram
    };
  }

  yarrowStalkMethod(): ZhouyiHexagram {
    return this.coinCastMethod();
  }

  private createTransformedHexagram(originalLines: HexagramLine[]): ZhouyiHexagram {
    const transformedLines: HexagramLine[] = originalLines.map(l => {
      const isYang = !l.text.includes('九');
      const newText = isYang ? '六' : '九';
      return {
        position: l.position,
        isChanging: false,
        text: newText
      };
    });

    const binaryStr = transformedLines.map(l => (l.text.includes('九') ? '1' : '0')).join('');
    const name = hexagramMap.get(binaryStr) || '未知卦';
    const meaning = hexagramMeanings[name] || '';
    const trigrams = ['乾', '坤', '震', '巽', '坎', '离', '艮', '兑'];
    const upperLines = transformedLines.slice(0, 3);
    const lowerLines = transformedLines.slice(3);
    const upperTrigram = trigrams[this.getTrigramIndex(upperLines)] || '乾';
    const lowerTrigram = trigrams[this.getTrigramIndex(lowerLines)] || '乾';
    const yaoTextArray = yaoTexts[name] || transformedLines.map((l, i) => `第${i + 1}爻：${l.text}`);
    const detailedLines = transformedLines.map((l, i) => ({
      ...l,
      text: yaoTextArray[i] || l.text
    }));
    let hexagramNumber = 0;
    for (const [key, val] of hexagramData) {
      hexagramNumber++;
      if (val === name) break;
    }
    return {
      upperTrigram,
      lowerTrigram,
      hexagramNumber,
      name,
      meaning,
      lines: detailedLines,
      changingLines: []
    };
  }

  private getTrigramIndex(lines: HexagramLine[]): number {
    const binary = lines.map(l => l.text.includes('九') ? 1 : 0).join('');
    const trigramMap: Record<string, number> = {
      '111': 0, '110': 1, '101': 2, '100': 3,
      '011': 4, '010': 5, '001': 6, '000': 7
    };
    return trigramMap[binary] ?? 0;
  }

  private generateAnswer(hexagram: ZhouyiHexagram): string {
    let changingInfo = hexagram.changingLines && hexagram.changingLines.length > 0
      ? `动爻在第${hexagram.changingLines.join('、')}爻，`
      : '无动爻，';
    let transformedInfo = '';
    if (hexagram.transformedHexagram) {
      transformedInfo = `之卦为${hexagram.transformedHexagram.name}卦（第${hexagram.transformedHexagram.hexagramNumber}卦），${hexagram.transformedHexagram.meaning}。`;
    }
    return `所得卦象为${hexagram.name}卦（第${hexagram.hexagramNumber}卦），${hexagram.meaning}。${changingInfo}卦辞曰：${hexagram.meaning}。${transformedInfo}${hexagram.lines ? hexagram.lines.filter(l => l.isChanging).map(l => l.text).join('；') : ''}`;
  }
}
